#!/bin/bash

# Скрипт для запуска конкретной задачи Celery

# Переходим в директорию проекта
cd "$(dirname "$0")"

# Активируем виртуальное окружение, если оно используется
# Раскомментируйте следующие строки, если используете virtualenv
# source venv/bin/activate

# Проверяем, что задача указана
if [ -z "$1" ]; then
    echo "Ошибка: Не указано имя задачи"
    echo "Использование: $0 <имя_задачи> [аргументы]"
    echo "Пример: $0 app_catalog.tasks.activate_combo_category"
    exit 1
fi

# Запускаем указанную задачу
echo "Запуск задачи: $1"
celery -A SoloPizza call "$@"

echo "Задача отправлена на выполнение"